    public function login(Request $request){
        
        if(Auth::guard('admin')->attempt([
            "email"=>$request->email,
            "password"=>$request->password,
        ])){
            return redirect()->route('admin_dashboard');
        }else{
            return redirect()->back()->with("error","Invalid email or password");
        }
        
    }
   

